def fetch_eoffice_data(url: str) -> dict:
    request = urllib.request.Request(url, headers={"User-Agent": "phonebooth-map-eoffice/1.0"})
    with urllib.request.urlopen(request, timeout=30) as response:
        html = response.read().decode("utf-8", errors="replace")

    match = re.search(r'<script type="application/ld\+json">(.*?)</script>', html, re.S)
    if not match:
        return {"status": "NO_JSONLD"}

    data = json.loads(match.group(1))
    addr = data.get("address") or {}
    geo = data.get("geo") or {}
    lat = geo.get("latitude")
    lng = geo.get("longitude")
    if lat is None or lng is None:
        return {"status": "NO_GEO"}

    postal = (addr.get("postalCode") or "").strip()
    prefecture = infer_prefecture_from_postal(postal) or (addr.get("addressRegion") or "").strip()
    address = build_address_from_postal(addr, prefecture)
    if prefecture and address and not address.startswith(prefecture):
        address = f"{prefecture}{address}"

    return {
        "status": "OK",
        "lat": str(lat),
        "lng": str(lng),
        "address": address,
        "prefecture": prefecture,
        "formatted_address": f"〒{postal} {address}" if postal else address,
        "telephone": data.get("telephone", ""),
    }


def is_eoffice_row(row: dict[str, str]) -> bool:
    url = row.get("詳細URL", "")
    return EOFFICE_HOST in url and row.get("サービス名", "") == "いいオフィス"


def main() -> int:
    parser = argparse.ArgumentParser(description="Enrich ii-office rows from e-office.space JSON-LD")
    parser.add_argument("--input", type=Path, default=DEFAULT_INPUT)
    parser.add_argument("--output", type=Path, default=None)
    parser.add_argument("--cache", type=Path, default=DEFAULT_CACHE)
    parser.add_argument("--limit", type=int, default=0)
    parser.add_argument("--delay", type=float, default=0.15)
    parser.add_argument("--dry-run", action="store_true")
    args = parser.parse_args()

    fieldnames, rows = read_csv(args.input)
    output_path = args.output or args.input

    for col in EXTRA_COLUMNS:
        if col not in fieldnames:
            fieldnames.append(col)

    targets = [r for r in rows if is_eoffice_row(r) and not r.get("住所", "").strip()]
    if args.limit > 0:
        targets = targets[: args.limit]

    if args.dry_run:
        print(f"対象: {len(targets)}件")
        for row in targets[:5]:
            print(f"- {row['施設名']} -> {row['詳細URL']}")
        return 0

    cache = load_cache(args.cache)
    ok = 0
    fail = 0

    for index, row in enumerate(targets, start=1):
        url = row["詳細URL"].strip()
        if url in cache:
            result = cache[url]
        else:
            try:
                result = fetch_eoffice_data(url)
                cache[url] = result
                save_cache(args.cache, cache)
                time.sleep(args.delay)
            except (urllib.error.URLError, json.JSONDecodeError, TimeoutError) as exc:
                result = {"status": f"ERROR:{exc}"}
                cache[url] = result
                save_cache(args.cache, cache)
                fail += 1
                continue

        if result.get("status") != "OK":
            fail += 1
            continue

        row["住所"] = result["address"]
        if result.get("prefecture"):
            row["都道府県"] = result["prefecture"]
        row["緯度"] = result["lat"]
        row["経度"] = result["lng"]
        row["ジオコード住所"] = result.get("formatted_address", result["address"])
        row["ジオコードステータス"] = "OK"
        row["ジオコードクエリ"] = url
        row["ジオコード方法"] = "e-office"
        ok += 1

        if index % 25 == 0 or index == len(targets):
            print(f"[{index}/{len(targets)}] ok={ok} fail={fail}")
            write_csv(output_path, fieldnames, rows)

    write_csv(output_path, fieldnames, rows)
    print(f"完了: ok={ok} fail={fail} -> {output_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
