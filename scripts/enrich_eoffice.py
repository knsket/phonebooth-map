def read_csv(path: Path) -> tuple[list[str], list[dict[str, str]]]:
    with path.open(encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = list(reader.fieldnames or [])
        rows = list(reader)
    return fieldnames, rows


def write_csv(path: Path, fieldnames: list[str], rows: list[dict[str, str]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)


def load_cache(path: Path) -> dict[str, dict]:
    if not path.exists():
        return {}
    return json.loads(path.read_text(encoding="utf-8"))


def save_cache(path: Path, cache: dict[str, dict]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(cache, ensure_ascii=False, indent=2), encoding="utf-8")


def normalize_hyphen(text: str) -> str:
    return text.replace("‑", "-").replace("−", "-").replace("–", "-").replace("—", "-")


def build_address_from_postal(addr: dict, prefecture: str) -> str:
    locality = normalize_hyphen((addr.get("addressLocality") or "").strip())
    street = normalize_hyphen((addr.get("streetAddress") or "").strip())
    street = re.sub(r"\s+", " ", street)

    if prefecture and street.startswith(prefecture):
        return normalize_hyphen(street)
    if locality and street.startswith(locality):
        return f"{prefecture}{street}" if prefecture else street
    if locality and locality in street and prefecture:
        return f"{prefecture}{street}"
    parts = [prefecture, locality, street]
    merged = normalize_hyphen("".join(p for p in parts if p))
    return re.sub(rf"^{re.escape(prefecture)}{re.escape(prefecture)}", prefecture, merged) if prefecture else merged


